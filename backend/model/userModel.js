import mongoose from "mongoose";

const bookUserSchema = new mongoose.Schema({
    fullname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true,
    }
}, { timestamps: true });


const user = mongoose.model("bookUser", bookUserSchema);
export default user